import BaseRoute from '../../../routes/base';

export default BaseRoute.extend({
  ////////////////////////////////////////
  // Properties
  ////////////////////////////////////////
  typeKey:            'organization',
  unloadOnDeactivate: false,
  ////////////////////////////////////////

  ////////////////////////////////////////
  // Lifecycle hooks
  ////////////////////////////////////////
  afterModel(model) {
    return this.store.query('organizationMembership', { organization_id: model.get('id') });
  }
  ////////////////////////////////////////
});



